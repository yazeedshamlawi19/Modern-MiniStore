<?php

/**
 * DB_HOST
 * اسم السيرفر (الخادم) الذي تعمل عليه قاعدة البيانات.
 * في بيئة التطوير المحلية (XAMPP / WAMP) يكون غالبًا "localhost".
 * يعني أن قاعدة البيانات موجودة على نفس الجهاز.
 */
define('DB_HOST', 'localhost');

/**
 * DB_NAME
 * اسم قاعدة البيانات الخاصة بالمشروع.
 * هذا الاسم يجب أن يكون مطابق تمامًا لاسم قاعدة البيانات في phpMyAdmin.
 */
define('DB_NAME', 'mini_market_project');

/**
 * DB_USER
 * اسم مستخدم قاعدة البيانات.
 * في localhost عادة يكون "root" بدون صلاحيات محدودة (للتجربة والتعليم).
 */
define('DB_USER', 'root');

/**
 * DB_PASS
 * كلمة مرور مستخدم قاعدة البيانات.
 * في أغلب بيئات localhost تكون فارغة.
 * (في السيرفر الحقيقي لا تكون فارغة أبدًا).
 */
define('DB_PASS', '');

/**
 * DB_CHARSET
 * الترميز المستخدم للتعامل مع النصوص داخل قاعدة البيانات.
 * utf8mb4 مهم جدًا لأنه يدعم:
 * - اللغة العربية
 * - الإيموجي
 * - جميع الرموز الحديثة
 */
define('DB_CHARSET', 'utf8mb4');

/**
 * BASE_URL
 * الرابط الأساسي للمشروع.
 * يُستخدم في:
 * - التحويل بين الصفحات (header Location)
 * - بناء روابط كاملة
 * - API requests
 * 
 * مهم جدًا أن ينتهي بشرطة مائلة /
 */
define('BASE_URL', 'http://localhost/mini_market_project/');

/**
 * date_default_timezone_set
 * تحديد المنطقة الزمنية للمشروع.
 * يؤثر على:
 * - date()
 * - time()
 * - NOW() في PHP
 * 
 * Asia/Hebron = توقيت فلسطين
 */
date_default_timezone_set('Asia/Hebron');

/**
 * ADMIN_USER
 * اسم مستخدم الأدمن.
 * يُستخدم فقط في صفحة تسجيل دخول المسؤول.
 * هذا حل تعليمي مبسّط وليس نظام صلاحيات متقدم.
 */
define('ADMIN_USER', 'admin');

/**
 * ADMIN_PASS
 * كلمة مرور الأدمن.
 * يتم التحقق منها مباشرة عند تسجيل الدخول.
 * (في المشاريع الحقيقية يتم تشفيرها).
 */
define('ADMIN_PASS', '1234');
